            }
        }

        /// <summary>
        /// Gets the path.
        /// </summary>
        /// <value>The path.</value>
        public string Path
        {
            get
            {
                if (uri == null) return string.Empty;

                var path = "/";

                foreach (var segment in uri.Segments)
                {
                    if (segment == "/")
                    {
                    }
                    else if (!segment.Contains("."))
                    {
                        path += segment;
                    }
                    else
                    {
                        break;
                    }
                }

                return path;
            }
        }

        /// <summary>
        /// Gets the sub domain.
        /// </summary>
        /// <value>The sub domain.</value>
        public string SubDomain
        {
            get
            {
                if (string.IsNullOrEmpty(subdomain))
                {
                    var matchedDomains = domains.FindAll(x => DomainSansWww.EndsWith(x));

                    var matchedDomain = matchedDomains.Count == 0
                        ? string.Empty
                        : matchedDomains.Aggregate(string.Empty, (max, cur) => max.Length > cur.Length ? max : cur);

                    var domainWithoutTld = DomainSansWww
                        .Replace(matchedDomain, string.Empty)
                        .Trim('.');

                    var parts = domainWithoutTld.Split('.');

                    subdomain = parts.Take(parts.Length - 1).Join(".");
                }

                return subdomain;
            }
        }

        /// <summary>
        /// Gets the domain sans sub domain.
        /// </summary>
        /// <value>The domain sans sub domain.</value>
        public string DomainSansSubDomain
        {
            get
            {
                return HasSubDomain ? DomainSansWww.Replace(SubDomain + ".", string.Empty) : DomainSansWww;
            }
        }

        /// <summary>
        /// Gets a value indicating whether this instance has sub domain.
        /// </summary>
